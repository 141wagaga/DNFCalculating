        super().被动倍率计算()
        # 秒伤

        self.技能栏[self.技能序号['念气环绕：御']].被动倍率 = 6 * self.技能栏[self.技能序号['念气环绕']].被动倍率 * self.技能栏[self.技能序号['念气环绕：御']].被动倍率
