    def 伤害指数计算(self):
        if self.死亡毒雾力智开关 == 1:
            self.进图力量 += self.技能栏[self.技能序号['死亡毒雾']].力智加成()
            self.进图智力 += self.技能栏[self.技能序号['死亡毒雾']].力智加成()
            self.固定减防 += self.技能栏[self.技能序号['死亡毒雾']].防御减少()
        for i in self.技能栏:
            if i.是否主动 == 1:
                i.涂毒倍率 *= (self.主BUFF - 1)
        super().伤害指数计算()
        self.伤害指数 /= self.主BUFF

    def 伤害计算(self, x = 0):
        
        self.所有属性强化(self.进图属强)
        # Will添加
        self.CD倍率计算()
        self.加算冷却计算()

        self.被动倍率计算()
        self.伤害指数计算()

        技能释放次数=[]
        技能单次伤害=[]
        技能总伤害=[]


        #技能释放次数计算
        for i in self.技能栏:
            if i.是否有伤害==1:
                if self.次数输入[self.技能序号[i.名称]] =='/CD':
                    技能释放次数.append(int((self.时间输入 - i.演出时间)/i.等效CD(self.武器类型) + 1 +i.基础释放次数))
                else:
                    技能释放次数.append(int(self.次数输入[self.技能序号[i.名称]]))
            else:
                技能释放次数.append(0)

        #毒伤结算丢失开关
        if self.毒伤结算丢失开关 == 1: 
            蛇拳 = ''
            蛇拳次数 = 3
            for n in ['万毒噬心诀x3', '万毒噬心诀x4', '万毒噬心诀x5']:
                if 技能释放次数[self.技能序号[n]] > 蛇拳次数:
                    蛇拳次数 = 技能释放次数[self.技能序号[n]]
                    蛇拳 = n
            if 蛇拳次数 > 3:
                self.技能栏[self.技能序号[蛇拳]].涂毒倍率 *= (蛇拳次数 - 3) / 蛇拳次数
   
            for n in ['裂地飞沙', '猛毒擒月炎','毒雷引爆','擒月炎']:
                次数 = 技能释放次数[self.技能序号[n]]
                if 次数 > 1:
                    丢失涂毒时间 = (次数 - 1) * self.技能栏[self.技能序号[n]].等效CD(self.武器类型) - self.时间输入 + 3 + 1 #1秒余地 例如：输入20S，18S技能放两次，会丢失2秒
                    if 丢失涂毒时间 > 0:
                        self.技能栏[self.技能序号[n]].涂毒倍率 *= 1 - 丢失涂毒时间 / (次数 * 3)  #接上面，丢失2秒，平均倍率为1-2/6=2/3，也即是6*2/3计算4秒涂毒伤害

        #技能单次伤害计算
        for i in self.技能栏:
            if i.是否有伤害==1:
                技能单次伤害.append(i.等效百分比(self.武器类型)*self.伤害指数*i.被动倍率)
            else:
                技能单次伤害.append(0)
      

        #单技能伤害合计
        for i in self.技能栏:
            if i.是否有伤害==1 and 技能释放次数[self.技能序号[i.名称]] != 0:
                技能总伤害.append(技能单次伤害[self.技能序号[i.名称]]*技能释放次数[self.技能序号[i.名称]]*(1+self.白兔子技能*0.20+self.年宠技能*0.10*self.宠物次数[self.技能序号[i.名称]]/技能释放次数[self.技能序号[i.名称]]+self.斗神之吼秘药*0.12))
            else:
                技能总伤害.append(0)
    
        总伤害=0
        for i in self.技能栏:
            总伤害+=技能总伤害[self.技能序号[i.名称]]
        
        if x==0:
            return 总伤害
    
        if x==1:
            详细数据=[]
            for i in range(0,len(self.技能栏)):
                详细数据.append(技能释放次数[i])
                详细数据.append(技能总伤害[i])
                if 技能释放次数[i]!=0:
                    详细数据.append(技能总伤害[i]/技能释放次数[i])
                else:
                    详细数据.append(0)
                if 总伤害!=0:
                    详细数据.append(技能总伤害[i]/总伤害*100)
                else:
                    详细数据.append(0)
            return 详细数据

